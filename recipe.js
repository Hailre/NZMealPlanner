function changeTextmethod(){
    var element = document.getElementById("selection");
    element.innerHTML = `<b>METHOD</b><br>
    Peel and dice the onion, then deseed and dice the peppers.<br> 
    Soften in 1 tablespoon of oil in a large pan over a low heat.<br>
    Peel, finely slice and add the garlic, along with the paprika and cumin, and cook for 1 to 2 minutes.<br> 
    Add the beef and stir until it has browned.<br>
    Pour in the stock, cover, and cook for 45 minutes, or until reduced and delicious.<br>
    Preheat the oven to 180ºC/350ºF/gas 4.<br>
    For the salsa, roughly chop the tomatoes, trim and finely slice the spring onion, then pick and roughly chop the coriander leaves.<br> 
    Combine with the lime juice, then season carefully to taste.<br>
    For the guacamole, halve and destone the avocados, then mash the flesh with a fork.<br> 
    Squeeze in the lime juice, add the crème fraîche, season, and gently mix it all up.<br>
    Spread the taco shells out on a baking tray and place in the oven for 3 to 4 minutes until crisp.<br>
    Fill the shells with the meat, salsa and guacamole or lay everything out and let everyone help themselves.`;
}

